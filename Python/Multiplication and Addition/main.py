def multipy(a, b):
    return a * b

numa = int(input("Enter the first number for multiplication: "))
numb = int(input("Enter the second number for multiplication: "))

result_mul = multipy(numa, numb)
print("The product is:", result_mul)


def add(x, y):
    return x + y

numx = int(input("Enter the first number for addition: "))
numy = int(input("Enter the second number for addition: "))

result_add = add(numx, numy)
print("The product is:", result_add)


